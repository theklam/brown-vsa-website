											<h2>Our Mission</h2>
											<p>Since 1986, the Brown-RISD Vietnamese Students Association has sought to promote the understanding and appreciation of the cultural, social, political, and historical identity of people of Vietnamese heritage by providing a safe space for discussion, exploration, and community building. Our activities include celebrating Vietnamese holidays, discussing current issues that are pertinent to Vietnamese and Vietnamese Americans, and participating in events that connect us to Vietnamese culture and traditions.</p>

											<h3>About Brown University</h3>
											<p>The seventh oldest college in the United States and a member of the Ivy League, Brown is well-known for its open curriculum and excellence in undergraduate education.  The University is comprised of undergraduate and graduate programs, as well as the Warren Alpert Medical School, School of Public Health, School of Engineering, and School of Professional Studies. Learn more about Brown <a href="https://www.brown.edu/about" target="new">here</a>.</p>

											<h3>About the Rhode Island School of Design</h3>
											<p>Founded in 1877, the Rhode Island School of Design (RISD) is one of the oldest and most respected colleges of art and design in the United States. RISD offers a creative, studio-based education at both the undergraduate and graduate levels. Learn more about RISD <a href="http://www.risd.edu/about" target="new">here</a>.</p>

											<h3>Bringing together the Brown and RISD communities</h3>
											<p>VSA was founded at Brown in 1986. Thirty years later, Brown VSA expanded its presence to RISD in order to better engage and serve the Vietnamese student community on College Hill. Today, Brown-RISD VSA is officially recognized by both Brown's Student Activities Office and RISD's Center for Student Engagement.</p>

